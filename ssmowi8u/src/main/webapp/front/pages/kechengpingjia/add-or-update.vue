					this.$utils.msg(`教学设计应输入整数`);
					return
				}
				if(this.ruleForm.jiaoxuefangfa&&(!this.$validate.isIntNumer(this.ruleForm.jiaoxuefangfa))){
					this.$utils.msg(`教学方法应输入整数`);
					return
				}
				if(this.ruleForm.jiaoxuexiaoguo&&(!this.$validate.isIntNumer(this.ruleForm.jiaoxuexiaoguo))){
					this.$utils.msg(`教学效果应输入整数`);
					return
				}
				if(this.ruleForm.id){
					await this.$api.update(`kechengpingjia`, this.ruleForm);
				}else{
					await this.$api.add(`kechengpingjia`, this.ruleForm);
				}
				this.$utils.msgBack('提交成功');
			},
			optionsChange(e) {
				this.index = e.target.value
			},
			bindDateChange(e) {
				this.date = e.target.value
			},
			getDate(type) {
				const date = new Date();
				let year = date.getFullYear();
				let month = date.getMonth() + 1;
				let day = date.getDate();
				if (type === 'start') {
					year = year - 60;
				} else if (type === 'end') {
					year = year + 2;
				}
				month = month > 9 ? month : '0' + month;;
				day = day > 9 ? day : '0' + day;
				return `${year}-${month}-${day}`;
			},
			toggleTab(str) {
				this.$refs[str].show();
			}
		}
	}
